import { readFileSync, writeFileSync } from "node:fs";
import { dirname, resolve } from "node:path";
import { fileURLToPath } from "node:url";
import prettier from "prettier";

const scriptDirectory = dirname(fileURLToPath(import.meta.url));
const repositoryRoot = resolve(scriptDirectory, "../../..");
const artifactRelativePath =
  "contracts/out/HarborRedeemer.sol/HarborRedeemer.json";
const artifactPath = resolve(repositoryRoot, artifactRelativePath);
const outputRelativePath = "packages/protocol/src/harborRedeemerArtifact.ts";
const outputPath = resolve(repositoryRoot, outputRelativePath);

const artifact = JSON.parse(readFileSync(artifactPath, "utf8"));

if (!Array.isArray(artifact.abi)) {
  throw new Error(`${artifactRelativePath} does not contain an ABI array`);
}

const source = await prettier.format(
  `// Generated by pnpm --filter @harbor/protocol generate:harbor-abi.
// Source: ${artifactRelativePath}
import type { Abi } from "./types.js";

export const harborRedeemerArtifactContractName = "HarborRedeemer" as const;
export const harborRedeemerArtifactPath = "${artifactRelativePath}" as const;

export const harborRedeemerAbi = ${JSON.stringify(
    artifact.abi,
    null,
    2,
  )} as const satisfies Abi;
`,
  { parser: "typescript" },
);

writeFileSync(outputPath, source);
console.log(`Wrote ${outputRelativePath}`);
